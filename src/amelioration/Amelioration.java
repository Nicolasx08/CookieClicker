package amelioration;

public class Amelioration {
    int cost=0;
    int clicParSec=0;
    int dps=0;
    int pourcentRabais=0;

    public int getCost() { return cost; }
    public void setCost(int cost) { this.cost = cost; }
    public int getClicParSec() { return clicParSec; }
    public void setClicParSec(int clicParSec) { this.clicParSec = clicParSec; }
    public int getDps() { return dps; }
    public void setDps(int dps) { this.dps = dps; }
    public int getPourcentRabais() { return pourcentRabais; }
    public void setPourcentRabais(int pourcentRabais) { this.pourcentRabais = pourcentRabais; }
}
